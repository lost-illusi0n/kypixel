package net.lostillusion.kypixel.api.entities

/**
 * An enum representing different Skyblock item categories Hypixel returns.
 */
enum class SkyblockCategory  {
    /**
     * The armour category.
     */
    ARMOR,

    /**
     * The misc category.
     */
    MISC,

    /**
     * The consumables category.
     */
    CONSUMABLES,

    /**
     * The blocks category.
     */
    BLOCKS,

    /**
     * The weapons category.
     */
    WEAPON,

    /**
     * The accessories category.
     */
    ACCESSORIES
}